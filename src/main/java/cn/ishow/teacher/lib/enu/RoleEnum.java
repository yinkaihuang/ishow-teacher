public enum  RoleEnum {
    STUDENT(1,"学生"),TEACHER(2,"老师"),MANAGER(3,"管理员");

    private int code;
    private String msg;

    RoleEnum(int code, String msg) {
        this.code = code;
        this.msg = msg;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
